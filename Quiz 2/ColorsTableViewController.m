- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *CellIdentifier = @"ColorCell";
    
    ColorTableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier forIndexPath:indexPath];
    
    NSInteger indexColor = (indexPath.row + 1);
    cell.colorLabel.text = [NSString stringWithFormat:@"%i", (int)indexColor];
    
    if (indexColor % 2){
        cell.backgroundColor =  [ UIColor blueColor ];
    }else{
        cell.backgroundColor =  [ UIColor redColor ];
    }
    
    return cell;
}

